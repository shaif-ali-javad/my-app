import { CameraView, useCameraPermissions } from 'expo-camera';
import React, { useState, useRef } from 'react';
import { Button, StyleSheet, Text, TouchableOpacity, View, Image } from 'react-native';

export default function App() {
  const [facing, setFacing] = useState('back');
  const [permission, requestPermission] = useCameraPermissions();
  const [capturedPhoto, setCapturedPhoto] = useState(null);
  const cameraRef = useRef(null);

  if (!permission) {
    // Camera permissions are still loading.
    return <View />;
  }

  if (!permission.granted) {
    // Camera permissions are not granted yet.
    return (
      <View style={styles.container}>
        <Text style={{ textAlign: 'center' }}>We need your permission to show the camera</Text>
        <Button onPress={requestPermission} title="Grant Permission" />
      </View>
    );
  }

  function toggleCameraFacing() {
    setFacing(current => (current === 'back' ? 'front' : 'back'));
  }


  async function takeOrRetakePhoto() {
    if (capturedPhoto) {
      setCapturedPhoto(null); // Clear the captured photo
    } else {
      const photo = await cameraRef.current.takePictureAsync(); // Capture the photo
      setCapturedPhoto(photo);
    }
  }
  

  return (
    <View style={styles.container}>
      <CameraView style={{ ...styles.camera, pointerEvents: 'auto' }} facing={facing} ref={cameraRef}>
        {capturedPhoto && <Image source={{ uri: capturedPhoto.uri }} style={styles.capturedImage} />}
        <View style={styles.buttonContainer}>
          <TouchableOpacity style={styles.button} onPress={takeOrRetakePhoto}>
            <Text style={styles.text}>{capturedPhoto ? 'Retake Photo' : 'Take Photo'}</Text>
          </TouchableOpacity>
        </View>
      </CameraView>
    </View>
  );
}

const styles = StyleSheet.create({
  container: {
    flex: 1,
    justifyContent: 'center',
  },
  camera: {
    flex: 1,
  },
  buttonContainer: {
    flex: 1,
    flexDirection: 'row',
    backgroundColor: 'transparent',
    margin: 64,
  },
  button: {
    flex: 1,
    alignSelf: 'flex-end',
    alignItems: 'center',
  },
  text: {
    fontSize: 24,
    fontWeight: 'bold',
    color: 'white',
  },
  capturedImage: {
    flex: 1,
    width: '100%',
    height: '100%',
  },
});
